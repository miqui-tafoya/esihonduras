            ¿Por qué las y los jóvenes necesitan recibir educación integral en sexualidad?
            La educación sexual integral responde a la urgente necesidad de brindar a las personas jóvenes información
            que les empodere para que tomen decisiones fundamentadas en lo que respecta a las relaciones y la
            sexualidad, ayudándolos a desenvolverse en un mundo donde la violencia y las desigualdades basadas en el
            género, los embarazos precoces y no deseados, y el VIH y otras infecciones de transmisión sexual (ITS)
            continúan planteando graves riesgos para su salud y bienestar. Asimismo, una educación integral de calidad
            en sexualidad deficiente o inexistente, adaptada a la edad y a la etapa de su desarrollo, expone a los niños
            y jóvenes a una situación de vulnerabilidad frente a las conductas sexuales negativas y a la explotación
            sexual.
            “La educación integral en sexualidad desempeña un papel esencial en la salud y el bienestar de los niños y
            jóvenes. Al aplicar un enfoque basado en los educandos no sólo proporciona a los niños y jóvenes,
            progresivamente y en función de su edad, una educación basada en los derechos humanos, la igualdad de
            género, las relaciones, la reproducción, el comportamiento sexual de riesgo y la prevención de enfermedades
            desde una perspectiva positiva, poniendo de relieve valores tales como el respeto, la inclusión, la no
            discriminación, la igualdad, la empatía, la responsabilidad y la reciprocidad”
            Por qué es importante la educación integral en sexualidad, disponible en
