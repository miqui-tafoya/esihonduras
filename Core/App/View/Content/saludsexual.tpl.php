                <p class="cabecera-uno">¿Educación sexual integral?, <br> ¿Qué es?</p>
                <p class="cuerpo-texto">La Organización Mundial de la Salud señala que la Educación sexual integral
                    (ESI) “brinda a los
                    jóvenes información precisa y apropiada para su edad sobre la sexualidad y su salud sexual y
                    reproductiva, la cual es fundamental para su salud y supervivencia”
                    <br>
                    <br>
                    La UNESCO define “Educación Integral en Sexualidad (EIS) es un proceso basado en un currículo para
                    enseñar y aprender sobre los aspectos cognitivos, emocionales, físicos y sociales de la sexualidad.
                    Su objetivo es preparar a los niños, las niñas y los y las jóvenes con conocimiento, habilidades,
                    actitudes y valores que los empoderan para: realizar su salud, bienestar y dignidad; desarrollar
                    relaciones sociales y sexuales respetuosas; considerar cómo sus decisiones afectan su bienestar y el
                    de los demás; y entender cuáles son sus derechos a lo largo de sus vida y asegurarse de protegerlos”
