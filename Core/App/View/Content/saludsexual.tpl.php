                <p class="cabecera-dos-ss">¿Es lo mismo sexo, salud sexual, sexualidad y derechos sexuales?</p>
                <p class="cuerpo-seis-uno">
                    Para empezar, por <span class="cuerpo-seis-blod">sexo</span> nos referimos a las características biológicas que nos definen y diferencian
                    entre lo masculino y lo femenino. <span class="hiper-dos"> <a href="https://www.who.int/es/health-topics/sexual-health#tab=tab_2" target="_blank">La Organización Mundial de la Salud (OMS)</a></span> ha señalado que estas
                    características biológicas no son excluyentes ya que hay personas que poseen características tanto
                    femeninas como masculinas.
                    <br><br>
                    La <span class="cuerpo-seis-blod">salud sexual</span> hace referencia al estado de bienestar físico, mental y social en relación con la
                    sexualidad. La <span class="hiper-dos"> <a href="https://www.who.int/es/health-topics/sexual-health#tab=tab_2" target="_blank">OMS</a></span>OMS señala que para que esto sea posible, “la salud sexual requiere un enfoque
                    positivo y respetuoso de la sexualidad y de las relaciones sexuales, así como la posibilidad de
                    tener experiencias sexuales placenteras y seguras, libres de toda coacción, discriminación y
                    violencia”.
                    <br><br>
                    Entonces, ¿dónde queda la sexualidad? <span class="cuerpo-seis-blod">La sexualidad</span> es parte importante de la salud sexual pero debe
                    considerarse en un sentido amplio, no como un elemento solitario.
                    <br><br>
                    De hecho, la salud sexual no se puede pensar o definir solo como una práctica sexual, debe pensarse
                    desde un sentido más amplio y como un elemento del derecho a la salud sexual.
                    <br><br>
                    En este sentido, la sexualidad es entendida como un aspecto central y continuo en la vida de una
                    persona, la cualabarca al sexo, las identidades y los papeles de género, el erotismo, el placer, la
                    intimidad, la reproducción y la orientación sexual. [La sexualidad] se vivencia y se expresa a
                    través de pensamientos, fantasías, deseos, creencias, actitudes, valores, conductas, prácticas,
                    papeles y relaciones interpersonales” <span class="hiper-dos"> <a href="https://www.who.int/es/health-topics/sexual-health#tab=tab_2" target="_blank">(OMS, 2006)</a></span>.
                    Y así, llegamos a los  <span class="cuerpo-seis-blod">derechos sexuales,</span> ya que la salud sexual no se puede alcanzar y mantener sin
                    el respeto y la protección de los derechos sexuales de todas las personas.
                    Los derechos sexuales son derechos humanos y se deben proteger y garantizar, eso implica satisfacer,
                    expresar tu sexualidad, disfrutar, contar con información y respetar los derechos de las y los
                    demás.

