                            que hemos vivido con nuestros padres o madres o en nuestra adolescencia lo actuemos ahora
                            con nuestras hijas e hijos, por eso es muy importante hacernos cargo de nuestras historias,
                            darnos cuenta que las adolescencias que ahora acompañamos tienen una historia distinta a la
                            nuestra.
                            Quizá quienes nos criaron, no tenían las mismas herramientas, estaban en otra época,
                            nosotras y nosotros aprendimos sobre sexualidad de acuerdo a nuestro contexto y situación de
                            vida.
                            Ahora tenemos acceso a información, podemos crear vínculos más fuertes, amorosos y de
                            sostén.

                            Si hay algo de mi propia historia que si quiera repetir porque fue positivo, puedo usarlo
                            como herramienta como madre o padre.
                        </p>
                    </div>

                    <button class="accordion"> 7. ¿Qué información sobre sexualidad se le debe dar a las hijas e hijos?
                        ¿Cómo saber cuál es la información adecuada?</button>
                    <div class="panel">
                        <p>Es posible que tu hija o hijo adolescente ya tenga información al respecto, por la escuela,
                            las amistades, el mundo virtual.
                            Es recomendable preguntar, ¿Qué sabe?, ¿Qué dudas tiene?, de esta manera podemos saber qué
                            tipo de información es la que tiene y donde debemos dar mayor información.
                            No siempre la información que reciben es la adecuada, en ocasiones puede estar distorsionada
                            o ser falsa, por eso es importante indagar, en lo que piensa y siente al respecto y
                            verificar que sea información real, no sesgada, adecuada, etc.

                            Podemos ir acompañando según la edad, las necesidades e inquietudes.
                        </p>
                    </div>

                    <button class="accordion"> 8. ¿Hablar sobre sexualidad previene situaciones de violencia y favorece
                        las relaciones sanas?</button>
                    <div class="panel">
                        <p>Sí, hablar sobre sexualidad ayuda a prevenir el abuso sexual a identificar situaciones de
                            violencia en las relaciones y colabora a construir relaciones más justas y sanas. Hablar de
                            sexualidad no solo es hablar de relaciones sexuales, también es hablar sobre respeto,
                            autoestima, autocuidado, placer, consentimiento, límites, igualdad, inclusión.
                            La educación sexual integral puede cambiar la manera de vivir de las personas.

                        </p>
