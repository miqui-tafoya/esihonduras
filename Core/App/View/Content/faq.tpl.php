                            proceder: busca ayuda.
                            Puedes acercarte a los centros de atención comunitaria y preguntar por alguna persona
                            profesional del área de psicología o de salud sexual.
                            También se recomienda hablar con otras madres o padres, para compartir sentires y pensares,
                            es importante acompañarnos en el proceso, así podemos descubrir que esto es difícil para
                            muchas personas adultas y que no estamos solas en el camino.
                            Busca material de apoyo que te pueda ayudar a actualizarte en temas de sexualidad, para
                            poder abordar temas que posiblemente sean de interés para tu hija o hijo.


                        </p>
                    </div>

