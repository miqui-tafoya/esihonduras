                        hijos</button>
                    <div class="panel">
                        <p>Nos pueden dar miedo una infinidad de cosas, que se pongan en peligro, que alguien les haga
                            daño, que tomen decisiones que les lastimen, que no les traten bien, que queden embarazadas
                            o que puedan embarazar a alguien, que puedan contagiarse de alguna ETS, etc. Es muy válido
                            sentir miedos y es importante trabajarlos para poder guiar, acompañar y evitar transferirles
                            nuestros miedos a nuestras hijas e hijos.</p>
                    </div>

                    <button class="accordion">3. ¿Cuál es mi función como mamá, papá o adulta en la sexualidad de mi
                        hijo o
                        hija?</button>
                    <div class="panel">
                        <p>Nuestra función es acompañar, estar cerca, hablar sobre las inquietudes, miedos, deseos,
                            riesgos.
                            Dejarles saber que como personas adultas podemos contenerles emocionalmente y que pueden
                            confiar en nosotras y nosotros para contarnos cualquier cosa que deseen.
                            También nuestra función es orientar, es decir, ayudarles a identificar cómo poner límites,
                            qué hacer si algo no se siente bien, cómo tomar decisiones adecuadas.
                            A final de cuentas la educación sexual nos toca como madres o padres y lo que les
                            transmitimos es muy importante.
                        </p>
                    </div>

                    <button class="accordion">4. ¿Es mejor prohibir para evitar riesgos?
                    </button>
                    <div class="panel">
                        <p>Es más recomendable hablar, construir habilidades para identificar riesgos, fortalecer el
                            vínculo con los hijos e hijas que prohibir.
                            La comunicación con hijas e hijos es una oportunidad para formar en sexualidad, en
                            autoestima, contribuir a formar un criterio de autocuidado y de construcción de relaciones
                            sanas, por eso es mejor acompañar.

                        </p>
                    </div>

