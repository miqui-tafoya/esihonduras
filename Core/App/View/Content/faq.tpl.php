                            preguntar dudas o hablar de sexualidad con nuestra madres y padres, no lo aprendimos de
                            manera “natural” como parte de nuestra cultura, por lo tanto estamos aprendiendo a hablar de
                            sexualidad con nuestras hijas e hijos.
